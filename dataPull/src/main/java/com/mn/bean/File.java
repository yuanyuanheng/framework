package com.mn.bean;

public class File {
	private String m_sFilename;
	private String m_sPath;
	private String m_sPattern;
	
	public String getM_sFilename() {
		return m_sFilename;
	}
	public void setM_sFilename(String m_sFilename) {
		this.m_sFilename = m_sFilename;
	}
	public String getM_sPath() {
		return m_sPath;
	}
	public void setM_sPath(String m_sPath) {
		this.m_sPath = m_sPath;
	}
	public String getM_sPattern() {
		return m_sPattern;
	}
	public void setM_sPattern(String m_sPattern) {
		this.m_sPattern = m_sPattern;
	}
	
}
